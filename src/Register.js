// src/Register.js
import React, { useState } from "react";
import { GoogleLogin } from "@react-oauth/google";
import "./Register.css";

const Register = () => {
  const [formData, setFormData] = useState({
    name: "",
    email: "",
    password: "",
    confirmPassword: "",
  });

  const handleInputChange = (e) => {
    const { name, value } = e.target;
    setFormData({ ...formData, [name]: value });
  };

  const handleSubmit = (e) => {
    e.preventDefault();
    if (formData.password !== formData.confirmPassword) {
      alert("Passwords do not match!");
      return;
    }
    console.log("Registering with:", formData);
    // TODO: Send registration data to backend
  };

  const handleGoogleRegisterSuccess = (response) => {
    console.log("Google Register Success:", response);
    // TODO: Handle Google registration (backend integration)
  };

  const handleGoogleRegisterFailure = () => {
    console.log("Google Register Failed");
  };

  return (
    <div className="register-container">
      <h2>Register</h2>
      <form onSubmit={handleSubmit}>
        <div className="form-group">
          <label>Name:</label>
          <input
            type="text"
            name="name"
            value={formData.name}
            onChange={handleInputChange}
            required
          />
        </div>
        <div className="form-group">
          <label>Email:</label>
          <input
            type="email"
            name="email"
            value={formData.email}
            onChange={handleInputChange}
            required
          />
        </div>
        <div className="form-group">
          <label>Password:</label>
          <input
            type="password"
            name="password"
            value={formData.password}
            onChange={handleInputChange}
            required
          />
        </div>
        <div className="form-group">
          <label>Confirm Password:</label>
          <input
            type="password"
            name="confirmPassword"
            value={formData.confirmPassword}
            onChange={handleInputChange}
            required
          />
        </div>
        <button type="submit" className="submit-button">Register</button>
      </form>

      <h3>OR</h3>
      <GoogleLogin onSuccess={handleGoogleRegisterSuccess} onError={handleGoogleRegisterFailure} />
    </div>
  );
};

export default Register;

